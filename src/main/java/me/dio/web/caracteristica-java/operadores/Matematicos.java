package me.dio.web.operadores;

public class Matematicos {

    public static void main(String[] args) {

        //Adição
        System.out.println("Adição: " + (0 + 1));

        //Subtração
        System.out.println("Subtração: " + (3 - 1));

        //Multiplicação
        System.out.println("Multiplicação: " + (3 * 1));

        //Divisão
        System.out.println("Divisão: " + (8 / 2));

        //Módulo
        System.out.println("Módulo: " + (8 % 2)); //Resto da divisão

        int numero = 10;
        //Multiplicando o numero *2 e já atribuindo a variável
        numero *=2;
        System.out.println(numero);
    }
}
